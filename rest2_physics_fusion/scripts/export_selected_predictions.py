from __future__ import annotations

import argparse
import subprocess
import sys
from pathlib import Path

import pandas as pd

ROOT = Path(__file__).resolve().parents[1]
SRC = ROOT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from rest2_physics_fusion.training.model_selection import dataset_key


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Export prediction CSVs for every row in a model_selection.csv generated by the station-level pipeline."
    )
    parser.add_argument("--model-selection-csv", required=True)
    parser.add_argument("--csv-dir", required=True)
    parser.add_argument("--checkpoint-root", required=True, help="Usually outputs/<pipeline>/selected_models")
    parser.add_argument("--output-dir", required=True)
    parser.add_argument("--window-steps", type=int, default=16)
    parser.add_argument("--batch-size", type=int, default=32)
    parser.add_argument("--device", default="cpu")
    parser.add_argument("--allow-missing", action="store_true")
    return parser.parse_args()


def resolve_csv(csv_dir: Path, dataset: str) -> Path:
    path = csv_dir / dataset
    if path.exists():
        return path
    if not dataset.endswith(".csv"):
        path = csv_dir / f"{dataset}.csv"
        if path.exists():
            return path
    raise FileNotFoundError(f"Cannot find dataset CSV for {dataset!r} under {csv_dir}")


def main() -> None:
    args = parse_args()
    selection = pd.read_csv(args.model_selection_csv)
    required = {"dataset", "target_column", "selected_model_type"}
    missing = sorted(required - set(selection.columns))
    if missing:
        raise ValueError(f"Model selection CSV is missing columns: {missing}")

    csv_dir = Path(args.csv_dir)
    checkpoint_root = Path(args.checkpoint_root)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    exported_rows = []

    for row in selection.to_dict(orient="records"):
        dataset = str(row["dataset"])
        target_column = str(row["target_column"])
        model_type = str(row["selected_model_type"])
        csv_path = resolve_csv(csv_dir, dataset)
        key = dataset_key(csv_path)
        checkpoint = checkpoint_root / key / target_column / model_type / "best.pt"
        output_csv = output_dir / f"{key}_{target_column}_{model_type}_predictions.csv"
        if not checkpoint.exists():
            message = f"Missing checkpoint: {checkpoint}"
            if args.allow_missing:
                print(f"skip={message}")
                continue
            raise FileNotFoundError(message)
        command = [
            sys.executable,
            str(ROOT / "scripts" / "export_predictions.py"),
            "--checkpoint",
            str(checkpoint),
            "--csv",
            str(csv_path),
            "--output",
            str(output_csv),
            "--target-column",
            target_column,
            "--window-steps",
            str(args.window_steps),
            "--batch-size",
            str(args.batch_size),
            "--device",
            args.device,
        ]
        subprocess.run(command, check=True)
        exported_rows.append(
            {
                "dataset": dataset,
                "target_column": target_column,
                "selected_model_type": model_type,
                "checkpoint": str(checkpoint),
                "prediction_csv": str(output_csv),
            }
        )

    manifest = pd.DataFrame(exported_rows)
    manifest_path = output_dir / "selected_prediction_manifest.csv"
    manifest.to_csv(manifest_path, index=False, encoding="utf-8-sig")
    print(f"manifest_csv={manifest_path}")
    if not manifest.empty:
        print(manifest.to_string(index=False))


if __name__ == "__main__":
    main()
